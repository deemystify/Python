#Example usage of both for and while loops in python

people = ['John', 'Brown']
for name in people:
    print()

    print(name)

index = 0;
while index < len(people):
     print(people[index])
     index = index + 1

print()


  # Both loops do the same thing. It's best to use the for loop which is a lot easier to remember. Nevertheless, if you must use the while loop, always remember to update the index. 
